// mostra cada item de uma lista de imagens:
const imgs = document.querySelectorAll('img')
imgs.forEach(function (item) {
  console.log(item)
})

// mostra uma lista de todas as imagens:
const imgs2 = document.querySelectorAll('img')
console.log(imgs2)

// mostra uma lista de todas as imagens:
const imgs3 = document.getElementsByTagName('img')
console.log(imgs3)
