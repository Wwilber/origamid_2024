// Operadores de Atribuição: forma de abreviar o nosso código:

var x = 5
var y = 10

console.log((x += y))
console.log((x -= y))
console.log((x *= y))
console.log((x /= y))
console.log((x %= y))
console.log((x **= y))

// Operadores Ternários: Abreviação de condicionais com if e else

var idade = 19
var podeBeber = idade >= 18 ? 'pode beber' : 'não pode beber'
console.log(podeBeber)
